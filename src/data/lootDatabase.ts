export const getLootById = (id: string): LootItem | undefined => {
  return LOOT_DATABASE.find(item => item.id === id);
};

export const getLootByRarity = (rarity: LootRarity): LootItem[] => {
  return LOOT_DATABASE.filter(item => item.rarity === rarity);
};

export const getDungeonLoot = (dungeonLevel: number, playerLevel: number): LootItem[] => {
  const availableLoot = LOOT_DATABASE.filter(item => 
    item.levelRequired <= playerLevel && 
    item.levelRequired >= dungeonLevel - 2
  );
  
  const loot: LootItem[] = [];
  
  // Roll for each available item
  availableLoot.forEach(item => {
    const roll = Math.random();
    // Increase drop chance based on player level
    const bonusChance = playerLevel * 0.01;
    const adjustedChance = Math.min(0.95, item.dropChance + bonusChance);
    
    if (roll <= adjustedChance) {
      loot.push(item);
    }
  });
  
  // Ensure at least 1-3 items drop
  if (loot.length === 0) {
    const commonItems = availableLoot.filter(i => i.rarity === 'common');
    if (commonItems.length > 0) {
      loot.push(commonItems[Math.floor(Math.random() * commonItems.length)]);
    }
  }
  
  // Limit drops
  return loot.slice(0, 5);
};